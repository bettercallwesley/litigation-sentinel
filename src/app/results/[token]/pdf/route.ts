import { NextRequest, NextResponse } from "next/server";
import { verifyResultsToken } from "@/lib/resultsToken";
import { getLatestAssessment } from "@/lib/assessmentCapture";
import {
  firstNameFromEmail,
  levelDescriptor,
  pillarBars,
  weakestLabels,
} from "@/lib/assessmentView";

// Print-optimized HTML, NOT a binary PDF. Content-Type text/html with print
// CSS so the browser "Save as PDF" produces a clean one-pager. This deliberately
// avoids adding a PDF library dependency to the platform (package.json has none).
export const runtime = "nodejs";
export const dynamic = "force-dynamic";

function esc(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

export async function GET(
  req: NextRequest,
  { params }: { params: { token: string } }
) {
  const email = (req.nextUrl.searchParams.get("email") || "").toLowerCase().trim();
  const token = params.token;

  if (!email || !verifyResultsToken(email, token)) {
    return new NextResponse("Not found", { status: 404 });
  }
  const result = await getLatestAssessment(email);
  if (!result) {
    return new NextResponse("Not found", { status: 404 });
  }

  const firstName = firstNameFromEmail(email);
  const level = result.maturity_level;
  const descriptor = levelDescriptor(level);
  const bars = pillarBars(result);
  const weakest = weakestLabels(result);

  const barsHtml = bars
    .map(
      (b) => `
      <div class="bar-row">
        <div class="bar-head"><span>${esc(b.label)}</span><span class="muted">${b.score.toFixed(
          1
        )} / 5</span></div>
        <div class="bar-track"><div class="bar-fill" style="width:${b.pct}%"></div></div>
      </div>`
    )
    .join("");

  const weakestHtml =
    weakest.length > 0
      ? `<div class="section"><div class="eyebrow">Where the fastest gains are</div><ul>${weakest
          .map((w) => `<li>${esc(w)}</li>`)
          .join("")}</ul></div>`
      : "";

  const html = `<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8" />
<meta name="viewport" content="width=device-width, initial-scale=1" />
<title>Litigation Readiness Results</title>
<style>
  @page { size: letter; margin: 0.6in; }
  * { box-sizing: border-box; }
  body { font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, Helvetica, Arial, sans-serif; color: #101828; margin: 0; padding: 32px; }
  .wrap { max-width: 680px; margin: 0 auto; }
  .eyebrow { letter-spacing: 2px; font-size: 11px; color: #667085; text-transform: uppercase; }
  h1 { font-size: 24px; margin: 6px 0 2px; }
  .sub { color: #475467; font-size: 14px; margin: 0 0 20px; }
  .level { text-align: center; border: 1px solid #d0d5dd; border-radius: 12px; padding: 20px; margin-bottom: 24px; }
  .level .num { font-size: 52px; font-weight: 800; color: #1d64d8; line-height: 1.1; }
  .level .num small { font-size: 20px; color: #98a2b3; }
  .level .title { font-size: 18px; font-weight: 700; margin-top: 2px; }
  .level .blurb { color: #475467; font-size: 13px; max-width: 480px; margin: 8px auto 0; }
  .section { margin-bottom: 20px; }
  .bar-row { margin-bottom: 12px; }
  .bar-head { display: flex; justify-content: space-between; font-size: 13px; margin-bottom: 4px; }
  .muted { color: #667085; }
  .bar-track { height: 9px; background: #eaecf0; border-radius: 5px; }
  .bar-fill { height: 100%; border-radius: 5px; background: #1d64d8; }
  ul { margin: 6px 0 0; padding-left: 18px; font-size: 13px; color: #344054; }
  li { margin-bottom: 4px; }
  .foot { margin-top: 28px; font-size: 11px; color: #98a2b3; border-top: 1px solid #eaecf0; padding-top: 12px; }
  @media print { body { padding: 0; } .noprint { display: none; } }
</style>
</head>
<body>
  <div class="wrap">
    <div class="eyebrow">Litigation Sentinel</div>
    <h1>Litigation readiness results</h1>
    <p class="sub">${esc(firstName)}, here is where your program stands today.</p>

    <div class="level">
      <div class="eyebrow">Maturity level</div>
      <div class="num">${level}<small> / 5</small></div>
      <div class="title">Level ${level}: ${esc(descriptor.title)}</div>
      <div class="blurb">${esc(descriptor.blurb)}</div>
    </div>

    <div class="section">
      <div class="eyebrow">By capability</div>
      ${barsHtml}
    </div>

    ${weakestHtml}

    <div class="foot">Confidential to ${esc(email)}. Generated by Litigation Sentinel.</div>
  </div>
</body>
</html>`;

  return new NextResponse(html, {
    status: 200,
    headers: { "Content-Type": "text/html; charset=utf-8" },
  });
}
